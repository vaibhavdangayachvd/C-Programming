#include <stdio.h>
#include <math.h>
int main()
{
    double x,y,n;
    printf("----------------------------------------------------------------------------\n");
    printf("x\t");
    for(x=0.1;x<=0.9;x+=0.1)
        printf("%4.1lf\t",x);
    printf("----------------------------------------------------------------------------\n");
    for(x=0.0;x<=9.0;++x)
    {
        printf("%.1lf\t",x);
        for(n=.1;n<=.9;n+=0.1)
        {
            y=(exp(-(x+n)));
            printf("%.2lf\t",y);
        }
    }
    printf("----------------------------------------------------------------------------\n");
}
